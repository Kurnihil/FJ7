//------------------------------------------------------------------------------
// Process command data 8E -
//------------------------------------------------------------------------------
void Cmd_8EData(void)
{
	if (KBHIStep == 2)
	{
		KBHIData1 = KBHIData;	// Address high byte
		KBHIData1 = KBHIData1 & 0x0F;
	}
	if (KBHIStep == 1)
	{
		Tmp_XPntr = ((KBHIData1 << 8) + KBHIData);	// Address low byte
		KBHIKDOR = *Tmp_XPntr;
	}
}


//------------------------------------------------------------------------------
// Process command data 8F -
//------------------------------------------------------------------------------
void Cmd_8FData(void)
{
	if (KBHIStep == 3)
	{
		KBHIData1 = KBHIData;	// Address high byte
		KBHIData1 = KBHIData1 & 0x0F;
	}
	if (KBHIStep == 2)
	{
		KBHIData2 = KBHIData;	// Address low byte
	}
	if (KBHIStep == 1)
	{
		KBHIData1 = KBHIData1 & 0x0F;
		Tmp_XPntr = ((KBHIData1 << 8) + KBHIData2);
		*Tmp_XPntr = KBHIData;
	}
}


