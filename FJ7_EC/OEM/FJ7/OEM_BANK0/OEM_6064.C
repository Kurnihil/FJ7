//------------------------------------------------------------------------------
// Process command data D9 - Get project information
//------------------------------------------------------------------------------
void Cmd_D9Data(void)
{
	switch (KBHIData)
	{
	case 1:	// Revision desciption
		SendVerFunction(P_rev);
		break;

	case 2:	// Platform information
		SendVerFunction(P_model);
		break;

	case 3:	// Last updated date
		SendVerFunction(P_date);
		break;

	case 4:	// Internal revision
		Data_To_Host_nWait(P_digit[0]);
		Data_To_Host(P_digit[1]);
		break;

	default:
		break;
	}
}


